package object_oriented_programming;

public class Example6_polymorphism {
	
	//integer data type function
	public int multiply3Numbers(int a, int b,int c) {
		return a*b*c;
	}
	
	//double data type  function
	public double multiply3Numbers(double a, double b,double c) {
		return a*b*c;
	}

}
